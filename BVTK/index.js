header_offset = 64;

function offset_top(element, offset = 0) {
    if(element === document.body) return offset;
    return element.offsetTop + offset_top(element.parentElement)
}

function stick() {
    if (window.pageYOffset > stick_offset) {
        $(stick_el).addClass("sticky");
    } else {
        $(stick_el).removeClass("sticky");
    }
}

var stick_el = document.getElementById("stick");
var stick_offset = offset_top(stick_el)-header_offset;
$(window).on("scroll", stick);

