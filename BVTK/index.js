function click_blur(element, on_focus, on_blur, focus_event="click", blur_event=""){
    element = $(element);
    $(window).on("click touchstart", function (e) {
        if(! $(e.target).closest(element).length) {
            on_blur(e);
        }
    });
    element.unbind(focus_event);
    element.on(focus_event, on_focus);
    if(blur_event){
        element.on(blur_event, on_blur)
    }
}

